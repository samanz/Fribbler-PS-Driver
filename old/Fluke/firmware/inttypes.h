/* For platforms where int is 32 bits */

#ifndef _INTTYPES_H_
#define _INTTYPES_H_

typedef unsigned char uint8_t;
typedef char int8_t;

typedef unsigned short uint16_t;
typedef short int16_t;

typedef unsigned int uint32_t;
typedef int int32_t;

#endif /* _INTTYPES_H_ */

