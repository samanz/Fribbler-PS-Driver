void reset_scribbler()
{  
  IOSET = S_RST;
  usleep(100);
  IOCLR = S_RST;
}

/*
 * Put the scribbler into program mode
 */ 
void program_mode()
{
  int i;
  int ch;
  
#ifdef SCRIB_DEBUG
  putstr("\r\nprogram mode");
  putstr("\r\n");  printbin(PINSEL0);
#endif

  // zero out PINSEL0 so that UART0 acts as regular GPIO
  PINSEL0 &= ~UART0_PINMASK;


  IODIR |= S_TXD;
  
  IOSET = S_RST;
  IOCLR = S_TXD;
  
  msleep(5);
  IOCLR = S_RST;

  msleep(100);

  IOSET = (S_TXD);  
  
  // set UART0 to UART mode
  uart0Init(B9600, UART_8N1, UART_FIFO_8); 
  
  // flush buffer
  for (i = 0; i < 100; i++)
    {
      usleep(10);
      ch = uart0Getch();
    }
}

/*
 * Send a byte and check the reply
 */
int send_and_recv(unsigned char v)
{
  int ch1;
  int ch2;
  unsigned char tc_v;
  
  // 2's complement of v
  tc_v = (~v) + 1;

#ifdef SCRIB_DEBUG

  putstr("\r\nsending a ");
  putstr(" ");
  printdec(v);

  putstr("\r\nexpect a ");
  putstr(" ");
  printdec(v);
  putstr(" ");
  printdec(tc_v);
#endif

  uart0Putch(v);
  //echo of v
  ch1 = uart0GetchBlock();
  // 2's complement of v
  ch2 = uart0GetchBlock();

#ifdef SCRIB_DEBUG

  putstr("\r\ngot a ");
  printdec(ch1);

  putstr("\r\ngot a ");
  printdec(ch2);
#endif

  if (ch2 != tc_v || ch1 != v) return -1;

  return 0;
}

int send_id()
{
  int ch;

  if (send_and_recv('B')) return -1;
  if (send_and_recv('S')) return -1;
  if (send_and_recv('2')) return -1;
  
  uart0Putch(0);
  ch = uart0GetchBlock();

  if (ch != 0) return -1;
  
  ch = uart0GetchBlock();

#ifdef SCRIB_DEBUG
  putstr("\r\nversion = "); printdec(ch);
#endif
  
  return 0;
}

int program_scribbler(unsigned char* buffer, int size)
{  
  int i,j;
  int ch;
  int fail = 0;

  //id_scribbler(); return 0;
  
#ifdef SCRIB_DEBUG

  putstr("\r\nabout to program scribbler; size = ");
  printdec(size);
#endif

  program_mode();

  if (send_id())
    {
      set_led(255);
      reset_scribbler();
      fail = 1;
    }
  else
    {
      //send slot number
      //uart0Putch(0);
      //ch = uart0GetchBlock();
      //putstr("\r\n Got echo ");
      //printdec(ch);	  

      for (i = 0; i < size; i += 18)
	{
	  // send 18 byte packets
	  for (j = 0; j < 18; j += 1)
	    {
	      uart0Putch(buffer[i+j]);
	    }

	  // receive echo
	  for (j = 0; j < 18; j++)
	    {
	      //uart0GetchBlock();
	      uart0GetchBlock();
	    }

	  //get ack
	  ch = uart0GetchBlock();
	  //printdec(ch);
	  if (ch != 0)
	    {
	      set_led(255);
	      reset_scribbler();
	      fail = 1;
	      return fail;
	    }
	}
      // we're done
      uart0Putch(0);	  
    }

  uart0Init(B38400, UART_8N1, UART_FIFO_8);  
  return fail;
}

